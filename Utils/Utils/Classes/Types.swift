typealias ClosureEmpty = () -> Void
typealias Closure<T> = (T) -> Void
typealias ClosureTwo<T, U> = (T, U) -> Void
typealias ClosureThree<T, U, V> = (T, U, V) -> Void
typealias ClosureFour<T, U, V, W> = (T, U, V, W) -> Void
typealias ClosureFive<T, U, V, W, X> = (T, U, V, W, X) -> Void
typealias ClosureSix<T, U, V, W, X, Y> = (T, U, V, W, X, Y) -> Void
typealias ClosureSeven<T, U, V, W, X, Y, Z> = (T, U, V, W, X, Y, Z) -> Void
